""" Винни-Пух попросил Вас посмотреть, есть ли в его стихах ритм. Поскольку
разобраться в его кричалках не настолько просто, насколько легко он их придумывает, Вам
стоит написать программу. Винни-Пух считает, что ритм есть, если число слогов (т.е. число
гласных букв) в каждой фразе стихотворения одинаковое. Фраза может состоять из одного
слова, если во фразе несколько слов, то они разделяются дефисами. Фразы отделяются друг
от друга пробелами. Стихотворение Винни-Пух вбивает в программу с клавиатуры. В ответе
напишите “Парам пам-пам”, если с ритмом все в порядке и “Пам парам”, если с ритмом все не
в порядке

Ввод:
пара-ра-рам рам-пам-папам па-ра-па-дам 

Вывод:
Парам пам-пам """


# функция для подсчета количества гласных букв в слове
def count_vowels(word):
    vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ"
    count = 0
    for letter in word:
        if letter in vowels:
            count += 1
    return count

# ввод стихотворения
poem = input("Введите стихотворение Винни-Пуха: ")

# разбиваем стихотворение на фразы
phrases = poem.split()

# считаем количество гласных букв в каждой фразе
vowels_per_phrase = [count_vowels(phrase) for phrase in phrases]

# проверяем, все ли значения в списке равны
if all(v == vowels_per_phrase[0] for v in vowels_per_phrase):
    print("Парам пам-пам")
else:
    print("Пам парам")